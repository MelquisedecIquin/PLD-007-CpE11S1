#include <iostream> 
using namespace std; 

int main() 
{ 
	int x = 4 , y = 2, sum,difference,product,quotient,remainder; 
	
	sum= x + y; 
	difference= x - y; 
	product= x * y; 
	quotient= x / y; 
	remainder= x % y; 

	cout <<"Sum is = " << sum << endl; 
	cout <<"Difference is = " << difference << endl; 
	cout <<"Product is = " << product << endl; 
	cout <<"Quotient is = " << quotient << endl; 
	cout <<"Remainder is = " << remainder << endl; 

return 0; 
} 
